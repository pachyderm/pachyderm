const displayIcon = (name, file, parent) => {
  const svgItem = document.createElement('div');
  svgItem.classList.add('svgItem');

  const svgName = document.createElement('p');
  svgName.textContent = name;

  const svgElement = document.createElement('img');
  svgElement.src = file;

  svgItem.appendChild(svgName);
  svgItem.appendChild(svgElement);
  parent.appendChild(svgItem);
};

const IconSvgContainer = document.getElementById('icon-svg-container');
Icons.forEach(({name, file}) => {
  displayIcon(name, file, IconSvgContainer);
});

const HPESvgContainer = document.getElementById('hpe-svg-container');
HPE.forEach(({name, file}) => {
  displayIcon(name, file, HPESvgContainer);
});

